entier = 42  # int
flottant = 3.14  # float
chaine = "Bonjour, monde!"  # str (chaîne de caractères)
booleen = True  # bool (valeur booléenne)
liste = [1, 2, 3, "quatre", 5.0]  # list
tuple_val = (1, "deux", 3.0, True)  # tuple
dictionnaire = {"nom": "Alice", "age": 30, "ville": "Paris"}  # dict (dictionnaire)

print(f"Valeur de int : {entier}")
print(f"Valeur de float : {flottant}")
print(f"Valeur de str : {chaine}")
print(f"Valeur de bool : {booleen}")
print(f"Valeur de list : {liste}")
print(f"Valeur de tuple : {tuple_val}")
print(f"Valeur de dict : {dictionnaire}")
